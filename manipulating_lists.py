#manipulating lists:
#Concatenating with +:
        
a = [1,2.3,6]
b = [55,8,9.3]
c = a + b
print(a,b,c)


#slicing:
    
print(c[1:4]) #second to fourth element
print(c[:4]) #first to third element
print(c[3:]) #fourth to last element
print(c[:]) #all elements in the list


